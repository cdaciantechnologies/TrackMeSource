package com.trackme.spring.model;

public class VehicleMasterMapping
{
  private String panicOn;
  private String acOn;
  private String vehicleNo;
  private String fuelFormula;
  
  public String getPanicOn()
  {
    return this.panicOn;
  }
  
  public void setPanicOn(String panicOn)
  {
    this.panicOn = panicOn;
  }
  
  public String getAcOn()
  {
    return this.acOn;
  }
  
  public void setAcOn(String acOn)
  {
    this.acOn = acOn;
  }
  
  public String getVehicleNo()
  {
    return this.vehicleNo;
  }
  
  public void setVehicleNo(String vehicleNo)
  {
    this.vehicleNo = vehicleNo;
  }
  
  public String getFuelFormula()
  {
    return this.fuelFormula;
  }
  
  public void setFuelFormula(String fuelFormula)
  {
    this.fuelFormula = fuelFormula;
  }
}
